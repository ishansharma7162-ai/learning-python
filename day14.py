# User se input liya aur int mein badla
age = int(input("Apni umar (Age) dalo: "))
print("Aapki umar hai:", age)

# If-Else ka magic
if(age >= 18):
    print("Haan bhai! Aap gaadi (drive) chala sakte ho. 🚗")
    print("License banwa lo jaldi.")
else:
    print("Nahi bhai! Abhi aap chhote ho, gaadi nahi chala sakte. ❌")
    print("Ghar par baitho aur coding seekho.")

print("Yeh line hamesha chalegi kyunki yeh if-else ke bahar hai.")


